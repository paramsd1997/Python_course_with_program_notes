# Logical Operators:
# Logical operators are used to combine multiple conditions.


# 1.and: Returns True if both conditions are True.

x = True
y = False
andd = x and y  # result will be False
print("comparing two values: ", andd)


# 2. or: Returns True if at least one of the conditions is True.
x = True
y = False
orr = x or y  # result will be True
print("comparing two values  : ", orr)


# 3. not: Returns the opposite of the condition.
x = True
nott = not x  # result will be False
print("comparing two values  : ", nott)





