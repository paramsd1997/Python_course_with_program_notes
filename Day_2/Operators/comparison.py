# 1. Equal (==): Checks if two values are equal.
x = 5
y = 5
eq = (x == y) # result will be True
print(eq)

# 2.Not Equal (!=): Checks if two values are not equal.
x = 4
y = 7
neq = (x != y)  # result will be True
print(neq)


# 3. Greater Than (>): Checks if the first value is greater than the second.
x = 8
y = 3
great = x > y  # result will be True
print(great)

# 4. Less Than (<): Checks if the first value is less than the second.
x = 5
y = 10
less = x < y  # result will be True

print("less than is: ",less)


# 5. Greater Than or Equal To (>=): Checks if the first value is greater than or equal to the second.
x = 8
y = 8
gt  = ( x >= y ) # result will be True
print("greater than equal : ",gt)


# Less Than or Equal To (<=): Checks if the first value is less than or equal to the second.
x = 6
y = 6
lt = (x <= y)  # result will be True

print("less than equal : ",lt)









