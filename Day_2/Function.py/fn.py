"""
Functions in Python can be thought of as reusable blocks of code that perform specific tasks or operations. 
They are like mini-programs within your program.

### Define: You create a function by giving it a name and specifying what it should do when called. This is like defining a recipe for a task you want to perform.

Call: To use the function, you "call" it by its name. When you call a function, it performs the actions you defined in its recipe.

Parameters: You can provide information (like ingredients in a recipe) to a function through parameters. Parameters are like variables that the function uses while performing its task.

Return: After the function has completed its task, it can "return" a result or answer, which you can use in your program.
    """

def add_numbers(a, b):
    result = a + b
    return result


print(add_numbers(3,7))
